public class CalculateResult {

	private double enterPay;
	private double outPay;
	private double enterPremium;
	private double outPremium;
	
	public double getEnterPay() {
		return enterPay;
	}
	public void setEnterPay(double enterPay) {
		this.enterPay = enterPay;
	}
	public double getOutPay() {
		return outPay;
	}
	public void setOutPay(double outPay) {
		this.outPay = outPay;
	}
	public double getEnterPremium() {
		return enterPremium;
	}
	public void setEnterPremium(double enterPremium) {
		this.enterPremium = enterPremium;
	}
	public double getOutPremium() {
		return outPremium;
	}
	public void setOutPremium(double outPremium) {
		this.outPremium = outPremium;
	}
	
}
